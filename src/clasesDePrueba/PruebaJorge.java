package clasesDePrueba;

public class PruebaJorge {
	private String nombre;
	private int edad;
	public PruebaJorge(String cadena,int anios){
		this.nombre = cadena;
		this.edad = anios;
	}
	
	public String getNombre(){
		return nombre;
	}
	
	public int getEdad(){
		return edad;
	}
}
